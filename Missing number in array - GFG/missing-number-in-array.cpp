class Solution{
  public:
    int missingNumber(vector<int>& array, int n) {
        // Your code goes here
        int sum =(n*(n+1))/2;
        int A2=0;
        for (int i=0;i<n-1;i++){
            A2=A2+array[i];
        }
        int A1=sum-A2;
        return A1;
    }
};

